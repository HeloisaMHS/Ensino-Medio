    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        String nome = "", senha = "", email = "";
        nome = jtNome.getText();
        email = jtEmail36.getText();
        senha = new String(jpSenha.getPassword());
        Perfil objperfil = new Perfil(nome, email, senha);
        Pattern p = Pattern.compile(".+@.+\\.[a-z]+");
        Matcher m = p.matcher(email);
        boolean matchFound = m.matches();

        if (!matchFound) {
            JOptionPane.showMessageDialog(null, "E-mail inválido!\nAdicione '@' e tente novamente");
        } else {
            objperfil.setVisible(true);
            dispose();
        }


    }//GEN-LAST:event_jbCadastrarActionPerformed

