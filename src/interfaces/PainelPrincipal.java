    private void jbLogar37ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbLogar37ActionPerformed
        String email = jtEmail37.getText();
        String senha = new String(jtSenha37.getPassword());
        BDusuario bd = new BDusuario();
        int codigo = -1;
        try { // faz a busca no BD
            codigo = bd.verificaUsuario(email, senha);
        } catch (InstantiationException ex) {
            System.err.println(ex);
        }
        if (codigo != -1) {   // Administrador do sistema
            // limpa os dados
            jtSenha37.setText("");
            jtEmail37.setText("");
            dispose();
            PainelAtividades obj = new PainelAtividades();
            String nome=bd.getGlobal();
            obj.Nome(nome);
            obj.setCod_u(codigo);
            obj.setNome(nome);
            obj.setVisible(this.rootPaneCheckingEnabled);

        } else { // Usuário não cadastrado
            JOptionPane.showMessageDialog(rootPane, "Usuário não cadastrado",
                    "Alerta de erro", JOptionPane.ERROR_MESSAGE);
            // Volta o cursor para o campo código
            jtSenha37.setText("");
            jtEmail37.selectAll();
            jtEmail37.requestFocusInWindow();
        }
    }//GEN-LAST:event_jbLogar37ActionPerformed

